#include "libft.h"

int		ft_indexof(char *s, char c)
{
	int	i;

	if (!s || !c)
		return (-1);
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (i);
		i++;
	}
	return (-1);
}
