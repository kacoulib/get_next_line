#include "libft.h"

char		*ft_strncpy(char *s1, const char *s2, size_t len)
{
	size_t	i;
	int		n;

	i = 0;
	n = 0;
	while (i < len)
	{
		if (s2[i] && !n)
			s1[i] = s2[i];
		else
		{
			s1[i] = '\0';
			n = 1;
		}
		i++;
	}
	return (s1);
}
