#include "libft.h"

char		*ft_strrchr(const char *s, int c)
{
	char	*last;

	last = NULL;
	while (*s != '\0')
	{
		if (*s == (char)c)
			last = (char *)s;
		s++;
	}
	return ((*s == (char)c) ? (char *)s : last);
}
